package org.ngi.enums;

/**
 *  This is the highlight mode enum used for setting highlight modes.
 * @author Tom
 */
public enum HighlightMode {
    /**
     * No Highlight.
     */
    NONE,
    /**
     * Normal mouse-over highlight.
     */
    NORMAL,
    /**
     * Warning highlight.
     */
    WARN,
    /**
     * Error highlight.
     */
    ERROR;
    
}
